    private void deleteBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBTNActionPerformed
        // to get the first word in the selected item, which is the subject_id
        String subjectFull = subjectCB.getSelectedItem().toString();
        String subject_id = subjectCB.getSelectedItem().toString().split(" ", 2)[0];
        String fullname = fullnameTF.getText();
        String student_id = studentIDTF.getText();

        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete "
                + student_id + " " + fullname + " from " + subjectFull);

        if (confirm == 0) {

            System.out.println("confirming delete");
            
            connect.deleteGrades(studentIDTF, subject_id, gradeOneTF, gradeTwoTF, gradeThreeTF, finalExamTF);
        }
    }//GEN-LAST:event_deleteBTNActionPerformed

