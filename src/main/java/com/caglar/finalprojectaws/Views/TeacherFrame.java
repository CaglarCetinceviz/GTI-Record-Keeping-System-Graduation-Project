    private void searchBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBTNActionPerformed
        // Clear all TextField before setting them
        clearSearchBTN();
        // to get the first word in the selected item, which is the subject_id   
        String subject_id = subjectCB.getSelectedItem().toString().split(" ", 2)[0];
        // connecting to database and searching for studend and grades
        connect.searchStudent(studentIDTF, fullnameTF);
        connect.searchGrades(subject_id, studentIDTF, gradeOneTF, gradeTwoTF, gradeThreeTF, finalExamTF, resultTF);
    }//GEN-LAST:event_searchBTNActionPerformed

